		<h1>Animals in Illinois 65 Million Years Ago</h1>
		<p>To discuss what creatures were present millions of years ago in what is now Illinois, we must first talk about what this area and the rest of the earth was like in that time.</p>

<p>First, the climate during this time was much warmer than it is today. Most of the area that is Illinois today was actually part of a swampy tropical forest.</p>


<p>North America did not look like it does today. A vast gulf of water separated most of eastern North America from the western mountains. North and South America were also separated by water.</p>

<p>Because of the warmer temperatures and ocean currents at that time, ice caps at the poles were not present.</p>

<p>This time period is thought to be near the end of the dinosaurs. Were dinosaurs in Illinois? Scientists think there might have been, but no fossil remains have been found. Part of the reason is that the rocks that would contain these fossils have been eroded away in most parts of the state. However, there is some research that is currently being done in southern Illinois in an area that still has some of these rock layers left.</p>

<p>What kind of animals might have been around 65 million years ago?</p>

<p>Small mammals may have been present. These animals were probably active at night, so they could avoid predators.</p>

<p><img alt="Ornithomimus" src="images/orni.gif" style="width: 271px; height: 194px;"></p>

<p>Dinosaurs that may have been in the eastern part of North America were different than those in western North America. Main types of dinosaurs in the east were smaller in size than those present in the West. A family of dinosaurs found in the east was the Ornithomimus.</p>

<p><img alt="Tyrannosaurus" src="images/trex.gif" style="width: 271px; height: 245px;"></p>

<p>Larger dinosaurs could be found in the western land areas, such as the Tyrannosaurus.</p>

